  String primaryLanguage = '';
  String secondaryLanguage = '';
  String translateTo = 'russian';
  void changeLanguageTo(String newLanguage) {
    translateTo = newLanguage;
    update();
    notifyListeners();
  }

  void update() async {
    if (primaryController.text.length == 0) {
      secondaryController.clear();

      return;
    }
    try {
      var translator = GoogleTranslator();
      Translation translation = await translator.translate(
        primaryController.text,
        from: 'auto',
        to: languageMap[translateTo.toLowerCase()].toString().toLowerCase(),
      );
      primaryLanguage = translation.sourceLanguage.toString();
      secondaryLanguage = translation.targetLanguage.toString();

      if (primaryLanguage == 'Automatic') {
        primaryLanguage = secondaryLanguage;
      }
      secondaryController.text = translation.text.toString();
      notifyListeners();
    } catch (e) {
      print(e);
    }
  }
